package viaggi_organizzati;

public class Partenza {
	
	String identificativoViaggio;
	String data;
	String siglaAeroporto;
	double prezzoPersona;
	
	public Partenza(String identificativoViaggio, String data, String siglaAeroporto, double prezzoPersona) {
		this.identificativoViaggio = identificativoViaggio;
		this.data = data;
		this.siglaAeroporto = siglaAeroporto;
		this.prezzoPersona = prezzoPersona;
	}
	
	

}
